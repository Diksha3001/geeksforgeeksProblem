package com.DaySix;
import java.util.*;
public class WhileLoopDemo {

	    public static void solve() {
	        Scanner sc = new Scanner(System.in);
	        // Your Code Here
	        int n=sc.nextInt();
	        while(n>=0)
	        {
	            
	            System.out.print(n+" ");
	            n--;
	        }
	        
	    }
	    public static void main(String args[])
	    {
	    	WhileLoopDemo.solve();
	    }
	}